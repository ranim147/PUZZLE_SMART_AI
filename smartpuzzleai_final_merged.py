def compute_similarity_stats(embeddings, labels):
    unique_labels = np.unique(labels)
    intra_sim, inter_sim = [], []

    for lbl in unique_labels:
        mask      = labels == lbl
        cls_emb   = embeddings[mask]
        other_emb = embeddings[~mask]

        if len(cls_emb) < 2:
            continue

        sim_mat = sk_cosine_similarity(cls_emb)
        np.fill_diagonal(sim_mat, 0)
        intra_sim.extend(sim_mat[sim_mat > 0].tolist())

        if len(other_emb) > 0:
            inter_sim.extend(
                sk_cosine_similarity(cls_emb[:5], other_emb[:20]).flatten().tolist()
            )

    print("\n📊 Statistiques de similarité cosinus :")
    print(f"   Intra-classe  (même émotion)   : {np.mean(intra_sim):.4f}")
    print(f"   Inter-classe  (émotions diff.)  : {np.mean(inter_sim):.4f}")
    print(f"   Séparation (Δ)                 : {np.mean(intra_sim) - np.mean(inter_sim):.4f}")

    return np.mean(intra_sim), np.mean(inter_sim)

