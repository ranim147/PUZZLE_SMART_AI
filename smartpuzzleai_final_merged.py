def generate_pairs_list(samples_list, n_pairs, seed=42):
    import random as _r
    rng = _r.Random(seed)
    pairs = []
    for _ in range(n_pairs):
        idx1 = rng.randint(0, len(samples_list) - 1)
        same = rng.random() > 0.5
        _, label1 = samples_list[idx1]
        candidates = [i for i, (_, l) in enumerate(samples_list)
                      if (l == label1) == same and i != idx1]
        if not candidates:
            continue
        idx2 = rng.choice(candidates)
        pairs.append((idx1, idx2, 0.0 if same else 1.0))
    return pairs

