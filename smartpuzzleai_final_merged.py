def compare_with_missing_piece(user_drawing):
    """
    Compare le dessin utilisateur avec la vraie pièce manquante
    et retourne si cela correspond ou non.
    """

    global saved_missing_piece

    if user_drawing is None:
        return "⚠️ Veuillez dessiner la pièce manquante."

    if saved_missing_piece is None:
        return "⚠️ Aucune pièce de référence trouvée."

    try:
        # ----------------------------------------------------
        # ImageEditor retourne souvent un dict
        # ----------------------------------------------------
        if isinstance(user_drawing, dict):
            user_drawing = user_drawing.get("composite", None)

        if user_drawing is None:
            return "⚠️ Aucun dessin détecté."

        # ----------------------------------------------------
        # Conversion image utilisateur
        # ----------------------------------------------------
        if isinstance(user_drawing, np.ndarray):
            user_img = Image.fromarray(
                user_drawing.astype("uint8")
            ).convert("RGB")
        else:
            user_img = user_drawing.convert("RGB")

        # ----------------------------------------------------
        # Conversion image cible (important ici)
        # saved_missing_piece peut être PIL ou numpy
        # ----------------------------------------------------
        if isinstance(saved_missing_piece, np.ndarray):
            target_img = Image.fromarray(
                saved_missing_piece.astype("uint8")
            ).convert("RGB")
        else:
            target_img = saved_missing_piece.convert("RGB")

        # ----------------------------------------------------
        # Uniformiser taille
        # ----------------------------------------------------
        user_img = user_img.resize((128, 128))
        target_img = target_img.resize((128, 128))

        user_array = np.array(user_img).astype(np.float32)
        target_array = np.array(target_img).astype(np.float32)

        # ----------------------------------------------------
        # Calcul du score de similarité
        # ----------------------------------------------------
        diff = np.mean(np.abs(user_array - target_array))

        # plus diff est petit → meilleur score
        score = max(0, 100 - (diff / 2))

        # ----------------------------------------------------
        # Interprétation intelligente
        # ----------------------------------------------------
        if score >= 80:
            verdict = "✅ Excellent ! Votre dessin correspond très bien."
        elif score >= 60:
            verdict = "👍 Bon travail ! Le dessin est assez proche."
        elif score >= 40:
            verdict = "🙂 Partiellement correct, peut être amélioré."
        else:
            verdict = "❌ Le dessin ne correspond pas assez à la pièce réelle."

        return f"""
🎯 Score de similarité : {score:.2f}%

{verdict}
"""

    except Exception as e:
        return f"Erreur : {str(e)}"


