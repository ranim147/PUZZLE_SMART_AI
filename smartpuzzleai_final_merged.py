# ====================== SIAMESE NETWORK v2 — ResNet50 + TripletLoss ======================
# Améliorations vs v1 :
#   • Backbone : ResNet50 (25M params) au lieu de ResNet50 → meilleure représentation
#   • Embedding dim : 256 (au lieu de 128)
#   • Projection head plus profonde avec BatchNorm
#   • TripletMarginWithDistanceLoss (cosine) au lieu de ContrastiveLoss
#   • Mixup léger sur les embeddings pour régularisation

import torch
import torch.nn as nn
import torch.nn.functional as F
from torchvision import models

class SiameseNetwork(nn.Module):
    def __init__(self, embedding_dim=256):
        super().__init__()
        backbone = models.resnet50(weights=models.ResNet50_Weights.DEFAULT)
        self.feature_extractor = nn.Sequential(*list(backbone.children())[:-1])  # [B, 2048, 1, 1]

        self.embedding_head = nn.Sequential(
            nn.Linear(2048, 512),
            nn.LayerNorm(512),
            nn.ReLU(inplace=True),
            nn.Dropout(p=0.3),
            nn.Linear(512, 256),
            nn.LayerNorm(256),
            nn.ReLU(inplace=True),
            nn.Linear(256, embedding_dim),
            nn.LayerNorm(embedding_dim),
        )

    def forward_once(self, x):
        feat = self.feature_extractor(x)       # [B, 2048, 1, 1]
        feat = torch.flatten(feat, 1)           # [B, 2048]
        emb  = self.embedding_head(feat)        # [B, 256]
        return F.normalize(emb, p=2, dim=1)     # L2-normé

    def forward(self, x1, x2=None, x3=None):
        e1 = self.forward_once(x1)
        if x2 is None:
            return e1
        e2 = self.forward_once(x2)
        if x3 is None:
            return e1, e2
        e3 = self.forward_once(x3)
        return e1, e2, e3


# ── Triplet Loss cosinus ─────────────────────────────────────────────────────
def cosine_distance(a, b):
    """Distance cosinus ∈ [0, 2]. Plus petit = plus similaire."""
    return 1.0 - F.cosine_similarity(a, b, dim=-1)

triplet_loss_fn = nn.TripletMarginWithDistanceLoss(
    distance_function=cosine_distance,
    margin=0.3,
    reduction="mean",
)

model     = SiameseNetwork(embedding_dim=256).to(device)
criterion = triplet_loss_fn

total_p     = sum(p.numel() for p in model.parameters())
trainable_p = sum(p.numel() for p in model.parameters() if p.requires_grad)

print("✅ Siamese Network v2 créé !")
print(f"   Backbone      : ResNet50 (pretrained ImageNet)")
print(f"   Embedding dim : 256")
print(f"   Loss          : TripletMarginWithDistanceLoss (cosine, margin=0.3)")
print(f"   Device        : {device}")
print(f"   Total params  : {total_p:,}")
print(f"   Trainable     : {trainable_p:,}")

"""## 2.2 Génération de Triplets (Anchor / Positive / Negative)

Remplace la génération de **paires** (Contrastive Loss) par des **triplets** :
- **Anchor** : image de référence classe A
- **Positive** : autre image classe A
- **Negative** : image classe B ≠ A

Le réseau apprend à rapprocher `(anchor, positive)` et éloigner `(anchor, negative)`.

"""

# ====================== GÉNÉRATION DE TRIPLETS (Anchor / Positive / Negative) ======================
# Génère des triplets (anchor, positive, negative) pour TripletMarginWithDistanceLoss

def load_image(img_path, transform):
    img = np.array(Image.open(img_path).convert("RGB"))
    return transform(image=img)["image"]


def get_triplet(samples_list, rng=None):
    """
    Retourne (anchor, positive, negative) :
    - anchor  : image de la classe A
    - positive: autre image de la même classe A
    - negative: image d'une classe B ≠ A
    """
    if rng is None:
        rng = random
    idx_a = rng.randint(0, len(samples_list) - 1)
    _, label_a = samples_list[idx_a]

    pos_candidates = [i for i, (_, l) in enumerate(samples_list) if l == label_a and i != idx_a]
    neg_candidates = [i for i, (_, l) in enumerate(samples_list) if l != label_a]

    if not pos_candidates or not neg_candidates:
        return None, None, None

    idx_p = rng.choice(pos_candidates)
    idx_n = rng.choice(neg_candidates)

    path_a, _ = samples_list[idx_a]
    path_p, _ = samples_list[idx_p]
    path_n, _ = samples_list[idx_n]

    img_a = load_image(path_a, train_transform)
    img_p = load_image(path_p, train_transform)
    img_n = load_image(path_n, train_transform)

    return img_a, img_p, img_n


def generate_fixed_triplets(samples_list, n_triplets, seed=42):
    """Pré-génère des triplets fixes (pour val stable)."""
    import random as _rnd
    rng = _rnd.Random(seed)
    triplets = []
    for _ in range(n_triplets):
        idx_a = rng.randint(0, len(samples_list) - 1)
        _, label_a = samples_list[idx_a]
        pos = [i for i, (_, l) in enumerate(samples_list) if l == label_a and i != idx_a]
        neg = [i for i, (_, l) in enumerate(samples_list) if l != label_a]
        if not pos or not neg:
            continue
        triplets.append((idx_a, rng.choice(pos), rng.choice(neg)))
    return triplets

print("✅ Génération de triplets prête !")

"""## 2.3 Training Loop — Triplet Loss + Warmup LR

- **80 epochs** max avec early stopping (patience=15)
- **Warmup linéaire** 5 epochs → LR stable
- **Cosine annealing** pour la descente
- Validation sur **triplets fixés** (reproductibles seed=42)
- Gradient clipping à 1.0

"""

# ====================== TRAINING LOOP v2 — Triplet Loss + Warmup + Cosine LR ======================
from torch.optim.lr_scheduler import CosineAnnealingWarmRestarts
import math

NUM_EPOCHS      = 80        # plus d'epochs pour convergence
LR              = 3e-4      # LR initial
WARMUP_EPOCHS   = 5         # warmup lineaire
PATIENCE        = 15
TRIPLETS_TRAIN  = 600       # triplets par epoch
TRIPLETS_VAL    = 300       # triplets val fixés

model     = SiameseNetwork(embedding_dim=256).to(device)
optimizer = torch.optim.AdamW(model.parameters(), lr=LR, weight_decay=1e-4)

# Warmup linéaire puis cosine annealing
def lr_lambda(epoch):
    if epoch < WARMUP_EPOCHS:
        return (epoch + 1) / WARMUP_EPOCHS
    progress = (epoch - WARMUP_EPOCHS) / max(1, NUM_EPOCHS - WARMUP_EPOCHS)
    return 0.5 * (1.0 + math.cos(math.pi * progress))

scheduler = torch.optim.lr_scheduler.LambdaLR(optimizer, lr_lambda)

# ── Pré-génère triplets de validation fixes ──────────────────────────────────
print("⏳ Pré-génération des triplets de validation (fixés pour stabilité)...")
val_samples_list = [full_val_dataset.samples[i] for i in val_idx]
fixed_val_triplets = generate_fixed_triplets(val_samples_list, TRIPLETS_VAL, seed=42)
print(f"   ✅ {len(fixed_val_triplets)} triplets val générés\n")

best_val_loss    = float("inf")
patience_counter = 0
history          = {"train": [], "val": []}

print(f"🚀 Entraînement Triplet Loss | {NUM_EPOCHS} epochs | LR={LR} | device={device}\n")

for epoch in range(NUM_EPOCHS):

    # ── TRAIN ──────────────────────────────────────────────────────────────
    model.train()
    train_loss  = 0.0
    count_train = 0

    for _ in range(TRIPLETS_TRAIN):
        img_a, img_p, img_n = get_triplet(train_dataset.samples)
        if img_a is None:
            continue

        e_a, e_p, e_n = model(
            img_a.unsqueeze(0).to(device),
            img_p.unsqueeze(0).to(device),
            img_n.unsqueeze(0).to(device),
        )
        loss = criterion(e_a, e_p, e_n)

        optimizer.zero_grad()
        loss.backward()
        torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
        optimizer.step()

        train_loss  += loss.item()
        count_train += 1

    # ── VALIDATION sur triplets FIXES ──────────────────────────────────────
    model.eval()
    val_loss  = 0.0
    count_val = 0

    with torch.no_grad():
        for idx_a, idx_p, idx_n in fixed_val_triplets:
            try:
                path_a, _ = val_samples_list[idx_a]
                path_p, _ = val_samples_list[idx_p]
                path_n, _ = val_samples_list[idx_n]

                img_a = load_image(path_a, val_transform)
                img_p = load_image(path_p, val_transform)
                img_n = load_image(path_n, val_transform)

                e_a, e_p, e_n = model(
                    img_a.unsqueeze(0).to(device),
                    img_p.unsqueeze(0).to(device),
                    img_n.unsqueeze(0).to(device),
                )
                val_loss  += criterion(e_a, e_p, e_n).item()
                count_val += 1
            except Exception:
                continue

    avg_train = train_loss / max(count_train, 1)
    avg_val   = val_loss   / max(count_val,   1)
    scheduler.step()

    history["train"].append(avg_train)
    history["val"].append(avg_val)

    # ── SAUVEGARDE ─────────────────────────────────────────────────────────
    if avg_val < best_val_loss:
        best_val_loss    = avg_val
        patience_counter = 0
        torch.save(model.state_dict(), f"{MODELS_DIR}/siamese_best.pth")
        tag = " ✅ sauvegardé"
    else:
        patience_counter += 1
        tag = f" (patience {patience_counter}/{PATIENCE})"

    if epoch % 5 == 0 or patience_counter == 0:
        lr_now = optimizer.param_groups[0]["lr"]
        print(f"Epoch {epoch+1:02d}/{NUM_EPOCHS} | Train: {avg_train:.4f} | Val: {avg_val:.4f} | LR: {lr_now:.2e}{tag}")

    if patience_counter >= PATIENCE:
        print(f"\n⏹️  Early stopping epoch {epoch+1}")
        break

