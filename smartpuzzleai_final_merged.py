# ── Transforms ────────────────────────────────────────────────────────────────
to_tensor_512 = T.Compose([
    T.Resize((512, 512)),
    T.ToTensor(),
    T.Normalize([0.5]*3, [0.5]*3),
])

def encode_image(pil_img):
    t = to_tensor_512(pil_img).unsqueeze(0).to(device)
    with torch.no_grad():
        lat = vae.encode(t).latent_dist.sample() * vae.config.scaling_factor
    return lat  # [1, 4, 64, 64]

def get_text_embeddings(prompt, negative_prompt=""):
    with torch.no_grad():
        ids = tokenizer(
            prompt, padding="max_length",
            max_length=tokenizer.model_max_length,
            truncation=True, return_tensors="pt"
        ).input_ids.to(device)
        emb = text_enc(ids)[0]
        neg_ids = tokenizer(
            negative_prompt, padding="max_length",
            max_length=tokenizer.model_max_length,
            truncation=True, return_tensors="pt"
        ).input_ids.to(device)
        neg_emb = text_enc(neg_ids)[0]
    return emb, neg_emb

